package model;

public class Menu {
	private int m_num;
	private String s_id;
	private String m_name;
	private int m_price;
	private String m_img;
	private String rec_menu;
	public int getM_num() {
		return m_num;
	}
	public void setM_num(int m_num) {
		this.m_num = m_num;
	}
	public String getS_id() {
		return s_id;
	}
	public void setS_id(String s_id) {
		this.s_id = s_id;
	}
	public String getM_name() {
		return m_name;
	}
	public void setM_name(String m_name) {
		this.m_name = m_name;
	}
	public int getM_price() {
		return m_price;
	}
	public void setM_price(int m_price) {
		this.m_price = m_price;
	}
	
	public String getM_img() {
		return m_img;
	}
	public void setM_img(String m_img) {
		this.m_img = m_img;
	}
	public String getRec_menu() {
		return rec_menu;
	}
	public void setRec_menu(String rec_menu) {
		this.rec_menu = rec_menu;
	}
	
}
